         <h2>Qué es el fútbol</h2>

         <p class="text-start">El fútbol es considerado un deporte de cooperación y oposición cuyo nombre toma su raíz de la palabra inglés británica football, 
            en español balompié. Es un deporte de dos grupos de once jugadores cuyas normas vienen supervisadas por varios árbitros. Este deporte requiere de una gran capacidad
             de atención, comunicación entre compañeros y cooperación por encima de los intereses personales de cada jugador.
            Es el deporte rey por excelencia. Un estudio reciente de la FIFA reveló una cifra superior a 240 millones de practicantes en el mundo.</p>

        <h3>Dónde nace el futbol</h3>
        <p class="text-start">Los primeros orígenes del futbol se remontan a los siglos III y II antes de la era cristiana, pero su año real de fundación se considera en 1863 
            con la creación de “The Football Association” El fútbol actual es de origen inglés y es documentado por vez primera en las islas británicas. Eso sí, las primeras reglas del fútbol
             británico distaban mucho de las actuales, el juego era algo anárquico y bastante violento. En esa época Italia pudo influir a los ingleses en sus códigos futbolísticos, con su famoso calcio florentino,
              pero realmente el fútbol actual tiene su momento de aparición ya en el siglo XIX.Es en 1848 que varios colegios ingleses crean el Código Cambridge, que supone la base reglamentaria del fútbol actual, y unos años mas tarde es Londres la primera en oficializar las primeras normas del fútbol actual.
             ​ ​Desde ese momento el deporte estrella no ha parado de crecer siendo el más popular del mundo y alcanzando 270 millones de representantes. Siendo la Copa Mundial de Fútbol el evento de mayor audiencia en toda la historia del deporte.</p>

          <h4>Por qué el fútbol es el deporte rey</h4>
          <p class="text-start">Millones de personas juegan a fútbol o participan pasivamente viendo los partidos favoritos del deporte estrella. Un deporte que consiste básicamente en golpear una pelota y que sin embargo fascina a todo el mundo, de cualquier raza,
             color o bandera. Es un deporte cargado de magnetismo que atrae y mueve masas, que son capaces de desplazarse a enormes distancias por ver competir y ganar a su equipo favorito.
