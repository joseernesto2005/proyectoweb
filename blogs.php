<p class="text-start">El fútbol es un deporte muy activo que mejora la resistencia y la velocidad, así como la coordinación motora y
 visoespacial para manejar al balón con los pies a la vez que se corre, realizar pases o marcar gol. También mejora la visión periférica, 
 la velocidad de reacción y la agilidad física Se juega mediante una pelota que se debe desplazar a través del campo con cualquier parte del
  cuerpo que no sean los brazos o las manos, y mayoritariamente con los pies (de ahí su nombre). El objetivo es introducirla dentro de la portería o arco contrario, acción que se denomina marcar un gol.</p>
  
  <img src="img/futbol2.jpg" height="200" width="120" align="right" class="rounded" alt="...">
  
  <p>El fútbol es un deporte relativamente simple que se basa en el marcar goles al equipo contrario. Los equipos se forman por diez jugadores y un arquero, aquel responsable de evitar que se anoten puntos en 
 contra de su propio equipo. Los partidos de fútbol duran 90 minutos con un intervalo de 15 minutos que divide a los tiempos en dos períodos de 45 minutos cada uno.</p>

